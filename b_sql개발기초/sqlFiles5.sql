#RDBMS(Related DataBase management System)
# : 관계형 데이터 베이스는 데이터를 테아블 형태로 저장하고, 테이블간의 관계를 정의히여 데이터를 관리하는 데이터베이스 시스템
# RDBMS의 특징 
#:테이블 (관계)로 데이터를저장 
# : 스키마를 통해 데이터를 관리 

#주요 용어 
#테이블(table)
#:테이블 저장되는 기본 되는 단위 - 열(속성)과행(레코드) 

#열(Column)
#: 데이터를 저장하는 세로의 구분 
#: 각 열은 특정 데이터 유형을 가짐 
 
#행 (row)
#데ㅣ터의 한 레코드를 나타냄 
#: 각 해은 고유한 데이터 세트를 가짐 

#키(key)
#:테이블에서 레코드를 식별하는데 데이터 유지 

